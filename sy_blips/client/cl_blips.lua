  
      Citizen.CreateThread(function()
        for k,v in pairs(Map) do
            local blip = AddBlipForCoord(v.Position) 
            SetBlipSprite (blip, v.id) -- https://docs.fivem.net/docs/game-references/blips/
            SetBlipDisplay(blip, 6)
            SetBlipScale  (blip, v.weight) -- trad = taille [ entre 0.0 et 1.0 ]
            SetBlipColour (blip, v.color) -- https://docs.fivem.net/docs/game-references/blips/#blip-colors
            SetBlipAsShortRange(blip, true)
            BeginTextCommandSetBlipName("STRING") 
            AddTextComponentString(v.name)
            EndTextCommandSetBlipName(blip)
            if v.ZoneBlip then -- true = activer la zone / false = desactiver la zone
                local zoneblip = AddBlipForRadius(v.Position, 800.0)
            SetBlipSprite(zoneblip,1)
            SetBlipColour(zoneblip, v.color)
            SetBlipAlpha(zoneblip,80) -- Intensit√© ZoneBlips [entre 0 et 100]
            end
        end
    end)

